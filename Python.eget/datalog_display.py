bus = smbus.SMBus(1)
cmd_mod = 0x80
dat_mod = 0x40
def send(addr, mode, data):

    bus.write_i2c_block_data(addr, mode, data)

def init(ADDRESS):
    send(ADDRESS, cmd_mod, [0xae])
    send(ADDRESS, cmd_mod, [0xd5])
